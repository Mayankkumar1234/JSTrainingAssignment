// Question 7: Advanced Mathematical Functions

const calculator = {
  square(a) {
    return Math.square(a);
  },
  sqrt(a) {
    return Math.sqrt(a);
  },
  log(a) {
    return Math.log10(a);
  },
  ln(a) {
    return Math.ln(a);
  },
  sin(a) {
    return Math.sin(a);
  },
  cos(a) {
    return Math.cos(a);
  },
  tan(a) {
    return Math.tan(a);
  },
  power(x, y) {
    return Math.pow(x, y);
  },
  circleArea(r) {
    return Math.PI * r * r;
  },
};